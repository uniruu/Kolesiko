const button = document.querySelector("#compliment-button");
const complimentText = document.querySelector("#compliment-text");
const complimentBox = document.querySelector(".compliment-box");
const particlesContainer = document.querySelector(".particles");

let previousIndex = -1;

// Возвращает случайный комплимент, отличный от предыдущего.
function getRandomCompliment() {
  let newIndex;

  do {
    newIndex = Math.floor(Math.random() * compliments.length);
  } while (newIndex === previousIndex && compliments.length > 1);

  previousIndex = newIndex;
  return compliments[newIndex];
}

function showCompliment() {
  complimentText.textContent = getRandomCompliment();

  // Перезапуск CSS-анимации при каждом клике.
  complimentBox.classList.remove("is-animated");
  void complimentBox.offsetWidth;
  complimentBox.classList.add("is-animated");
}

// Добавляем фоновым частицам разный размер, положение и скорость.
function createParticles() {
  const symbols = ["♥", "✦", "✿", "♡", "✧"];

  for (let index = 0; index < 16; index += 1) {
    const particle = document.createElement("span");
    particle.className = "particle";
    particle.textContent = symbols[index % symbols.length];
    particle.style.left = `${Math.random() * 100}%`;
    particle.style.setProperty("--size", `${12 + Math.random() * 18}px`);
    particle.style.setProperty("--duration", `${11 + Math.random() * 11}s`);
    particle.style.setProperty("--delay", `${-Math.random() * 16}s`);
    particlesContainer.appendChild(particle);
  }
}

button.addEventListener("click", showCompliment);
createParticles();
